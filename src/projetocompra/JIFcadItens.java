    private void jtfPesquisaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtfPesquisaKeyReleased
        try {
            listaitens = DAO.pesquisar(jtfPesquisa.getText());
            DefaultTableModel mp = (DefaultTableModel)jtbDados.getModel();
            mp.setNumRows(0);
            listaitens.forEach((u) -> {
                mp.addRow(new String[]{""+u.getNome()});
            //    mp.addRow(Integer.parseInt(""+u.getValor(),u.getEstadoitem()));
            });
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao pesquisar:"+e.getMessage());
        }
    }//GEN-LAST:event_jtfPesquisaKeyReleased


