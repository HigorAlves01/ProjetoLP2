private boolean validarCampos(){
    String msg="";
    if(jtNome.getText().length()<3 || jtNome.getText().length()>100);
    if(jtValor.getText().length()>1);
    if(jcbestadoitem.getSelectedIndex()==0)
    if(msg.isEmpty()) return true; 
    else JOptionPane.showMessageDialog(this, "Os campos abaixo precisam de atenção:"
                +msg,"Atenção",JOptionPane.ERROR_MESSAGE);
 return false;
}
private boolean preencherObjeto() throws Exception {
    obj.setNome(jtNome.getText());
    obj.setValor(Integer.parseInt(jtValor.getText()));
    obj.setEstadoitem(jcbestadoitem.getSelectedIndex());
    return true;
}
