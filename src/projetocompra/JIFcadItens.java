    private void JBIsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBIsalvarActionPerformed
       try{
            if(validarCampos()){
            if(acao==1){
                    if(preencherObjeto()){
                        if(DAO.incluir(obj)){
                            JOptionPane.showMessageDialog(this, "Salvo com sucesso!",
                                    "Meu sistema", JOptionPane.INFORMATION_MESSAGE);
                            jtNome.setText("");
                            jtValor.setText("");
                            jcbestadoitem.setSelectedIndex(0);
                        }
                    }
            }
            }
                     }catch(Exception erro){
                JOptionPane.showMessageDialog(this,"Erro ao salvar:"+erro.getMessage(),
                       "Sistema",JOptionPane.ERROR_MESSAGE);
                }       
    }//GEN-LAST:event_JBIsalvarActionPerformed

